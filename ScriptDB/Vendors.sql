BEGIN

	CREATE TABLE #Regions
	(
		RegionId INT,
		RegionTypeId INT,
		RegionName NVARCHAR(500),
		Population INT
	)

	CREATE TABLE #VendorNew
	(
		VendorName INT,
		RegionId INT,
		IsDomestic BIT
	)

	INSERT INTO #Regions
	SELECT *, ABS(Checksum(NewID()) % (10000000 - 5000000)) + 5000000 Population FROM 
	(
		SELECT DISTINCT 15 ParentId, 2 TypeId, ProducingCountry FROM #Vendors
			WHERE (ProducingCountry LIKE N'%край%' OR ProducingCountry LIKE N'%обл.%' OR ProducingCountry LIKE N'%область%' OR ProducingCountry LIKE N'%Республика%')
			AND ProducingCountry != N'Московская обл.'
	) obl
		UNION ALL
	SELECT ABS(Checksum(NewID()) % (34 - 19)) + 19, 3, ProducingCountry, ABS(Checksum(NewID()) % (1000000 - 500000)) + 500000 FROM
	(
		SELECT DISTINCT ProducingCountry FROM #Vendors
			WHERE ProducingCountry NOT LIKE N'%край%' AND ProducingCountry NOT LIKE N'%обл.%' AND ProducingCountry NOT LIKE N'%область%' AND ProducingCountry NOT LIKE N'%Республика%'
