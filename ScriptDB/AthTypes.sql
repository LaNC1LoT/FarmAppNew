IF NOT EXISTS (SELECT TOP 1 1 FROM dist.CodeAthTypes)
BEGIN

	DECLARE @count INT = 0, @i INT = 1, @code NVARCHAR(500), @id INT

	SET @count = (SELECT COUNT(*) FROM #CodeAthTypes)

	WHILE @i <= @count
	BEGIN
		SELECT @code = Code FROM #CodeAthTypes WHERE Id = @i

		SELECT TOP 1 @id = Id FROM #CodeAthTypes WHERE (Code = LEFT(@code, 1) OR Code = LEFT(@code, 2) 
			OR Code = LEFT(@code, 3) OR Code = LEFT(@code, 4) OR Code = LEFT(@code, 5)) AND LEN(Code) != LEN(@code)
		ORDER BY Code desc

		UPDATE #CodeAthTypes SET CodeAthId = @id WHERE Id = @i

		SET @i += 1;
	END

	UPDATE #CodeAthTypes SET CodeAthId = NULL WHERE LEN(Code) = 1

	UPDATE #CodeAthTypes
	SET NameAth = UPPER(LEFT(NameAth, 1)) + LOWER(SUBSTRING(NameAth, 2, LEN(NameAth)))

	SET IDENTITY_INSERT dist.CodeAthTypes ON
	INSERT INTO dist.CodeAthTypes (Id, CodeAthId, Code, NameAth)
		SELECT Id, CodeAthId, Code, NameAth FROM #CodeAthTypes
	SET IDENTITY_INSERT dist.CodeAthTypes OFF

END


